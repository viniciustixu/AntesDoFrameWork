/* As regras de nomenclatura de variáveis em JavaScript são as seguintes:

1. Os nomes de variáveis devem começar com uma letra (a-z, A-Z) ou com o caractere de sublinhado (_), ou com o símbolo do dólar ($). Eles não podem começar com um número.

2. Os nomes de variáveis podem conter letras, números, sublinhados (_) ou símbolos do dólar ($). No entanto, não é recomendado usar o símbolo do dólar no início de um nome
de variável, pois geralmente é reservado para uso interno de bibliotecas JavaScript.

3. Os nomes de variáveis são case-sensitive, o que significa que "myVariable" e "myvariable" são considerados nomes de variáveis diferentes.

4. Não é possível usar palavras reservadas do JavaScript como nomes de variáveis. Por exemplo, as palavras "var", "function" e "if" são palavras reservadas e não podem ser usadas
como nomes de variáveis.

5. É recomendado usar nomes descritivos e significativos para suas variáveis, para que seu código seja mais legível e compreensível. Por exemplo, em vez de usar um nome como "x",
é melhor usar algo como "count" ou "total". */

