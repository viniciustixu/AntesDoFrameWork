var x = "variável 1" // var's tem escopo global. Var's são içadas (hoisted) o que significa que elas podem ser usadas antes de serem declaradas no código.

let y = "variável 2" // let's tem escopo de bloco e podem ser reatribuidas com um novo valor assim como as var's.

const z = "variável 3" /* const's tem escopo de bloco e não podem ser reatribuidas, no entanto, isso não significa que as variáveis const são imutáveis, pois se forem declaradas como objetos ou arrays, suas propriedades ou elementos podem ser alterados. */
